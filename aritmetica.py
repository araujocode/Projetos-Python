numeroUm = int(input("Digite um número inteiro: "))
numeroDois = int(input("Digite outro número inteiro: "))


soma = numeroUm + numeroDois
print("Soma:", soma)

subtracao = numeroUm - numeroDois
print("Subtração:", subtracao)

divisao = float(numeroUm / numeroDois)
print("Divisão:", "{:.2f}".format(divisao))

multiplicacao = numeroUm * numeroDois
print("Multiplicação:", multiplicacao)

potencia = numeroUm ** numeroDois
print("Potencia:", potencia)

raizQuadrada = float (numeroUm ** 0.5)
print("Raiz quadrada:", "{:.2f}".format(raizQuadrada))